#include<cstring>
#include<iostream>
#include<vector>
#include<algorithm>
using namespace std;
#define FIO ios_base::sync_with_stdio(0); cin.tie(0);
int main() {
    FIO;
    int n;
    cin >> n;
    vector<int> v(n);
    int sum = 0;
    for (int i = 0; i < n; i++) {
        cin >> v[i];
        sum += v[i];
    }

    int price;
    cin >> price;

    if (sum <= price) {
        cout<<*max_element(v.begin(), v.end());
        return 0;
    }

    int s = 0;
    int e = *max_element(v.begin(), v.end());
    int result = 0;
    while (s <= e) {
        const int m = (s + e) / 2;

        int c_sum = 0;
        for (int i=0;i<n;i++) {
            c_sum += min(v[i], m);
        }
        // cout << s << " " << e << " " << m << " " << c_sum << "\n";


        // м§ҖкёҲ л§Ңл“  к°’мңјлЎң кі„мӮ°мқ„ н–ҲлҚ”лӢҲ
        if (c_sum <= price) {
            s = m + 1;
            result = m;
        }else {
            e = m - 1;
        }
    }
    cout << result << "\n";

}