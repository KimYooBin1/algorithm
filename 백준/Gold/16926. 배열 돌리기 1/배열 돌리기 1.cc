#include<cstring>
#include<iostream>
#include<vector>
#include<deque>
using namespace std;
#define FIO ios_base::sync_with_stdio(false); cin.tie(NULL);
int dx[4] = {0, 1, 0, -1};
int dy[4] = {1, 0, -1, 0};
int main(){
    FIO;
    int n, m, r;
    cin >> n >> m >> r;
//    r %= 2*n + 2*m;
    vector<vector<int>> map(n, vector<int> (m));
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            cin>>map[i][j];
        }
    }

    int repeat = min(n, m) / 2;
    // лӮҙл¶ҖлЎң м–јл§ҲлӮҳ л“Өм–ҙк°Җм•јн•ҳлҠ”м§Җ
    for (int dep = 0; dep < repeat; dep++) {
        // лӘҮлІҲ нҡҢм „н•ҳлҠ”м§Җ
        for(int rot = 0; rot < r; rot++){
            int x = dep; int y = dep;
            int tmp = map[x][y];
            int k = 0;
            while (k < 4) {
                int nx = x + dx[k];
                int ny = y + dy[k];
                // мқҙкұ° м—Ҷм–ҙлҸ„ лҗ кұ° к°ҷмқҖлҚ°
//                if(nx == dep && ny == dep) break;
                if(nx < dep || ny < dep || n - dep <= nx || m - dep <= ny) k++;
                else{
                    map[x][y] = map[nx][ny];
                    x = nx, y = ny;
                }
            }
            // vector rotateлЎң н’Җм–ҙліҙкё°
            // лҳҗлҠ” dequeue
            map[dep + 1][dep] = tmp;
        }
    }


    for (auto &a: map) {
        for (auto &b: a) {
            cout << b << " ";
        }
        cout << "\n";
    }
    cout << "\n";
}