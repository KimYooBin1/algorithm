#include<cstring>
#include<iostream>
#include <map>
#include<vector>
#include<unordered_map>
#include<algorithm>
using namespace std;
#define FIO ios_base::sync_with_stdio(false); cin.tie(NULL);
struct L {
    int i, j, s;
};
int main(){
    FIO;
    int T;
    cin>>T;
    while (T--) {
        // n: нҢҖ к°ңмҲҳ k : л¬ём ң к°ңмҲҳ t : мҡ°лҰ¬ нҢҖ id
        int n, k, t, m;
        // key : нҢҖ, л¬ём ң value : м җмҲҳ
        map<pair<int, int>, int> score;
        // л§Ҳм§Җл§ү м ңм¶ң нҡҹмҲҳ
        unordered_map<int, int> order;
        unordered_map<int, int> count;
        cin >> n >> k >> t >> m;
        vector<L> l(m);
        for (int i = 0; i < m; i++) {
            // i : нҢҖ j : л¬ём ң лІҲнҳё s : мҠӨмҪ”м–ҙ
            cin >> l[i].i >> l[i].j >> l[i].s;
            // к°Ғ нҢҖмқҳ м ңм¶ң нҡҹмҲҳ
            count[l[i].i]++;
            // к°Ғ нҢҖмқҙ н‘ј л¬ём ңмқҳ м җмҲҳ
            score[{l[i].i, l[i].j}] = max(score[{l[i].i, l[i].j}] ,l[i].s);
            // к°Ғ нҢҖмқҳ л§Ҳм§Җл§ү м ңм¶ң мқёлҚұмҠӨ
            order[l[i].i] = i;
        }

        vector<pair<int,int>> v(n + 1, {0, 0});
        for (int i = 1; i <= n; i++) {
            v[i].first = i;
            // cout<<v[i].first<<"\n";
            for (int j = 1; j <= k; j++) {
                v[i].second += score[{i, j}];
            }
        }

        v.erase(v.begin());



        sort(v.begin(), v.end(), [&](const pair<int,int> &a, const pair<int,int> &b) {
            if (a.second == b.second) {
                if (count[a.first] == count[b.first]) {
                    return order[a.first] < order[b.first];
                }
                return count[a.first] < count[b.first];
            }
            return a.second > b.second;
        });
        for (int i=0;i<n;i++) {
            // cout<<v[i].first<<" "<<v[i].second<<"\n";
            if (v[i].first == t) {
                cout << i + 1 << "\n";
            }
        }
    }
}
