#include<cstring>
#include<iostream>
#include<queue>
#include<vector>
using namespace std;
#define FIO ios_base::sync_with_stdio(false); cin.tie(NULL);
int main() {
    FIO;
    int n, m;
    cin >> n >> m;
    vector<vector<int>> v(n + 1);
    for (int i = 0; i < m; i++) {
        int from, to;
        cin>>from>>to;
        v[from].push_back(to);
        v[to].push_back(from);
    }
    int result = 0;

    queue<int> q;
    q.push(1);
    vector<int> check(n + 1, -1);
    check[1] = 0;
    while (!q.empty()) {
        int x = q.front(); q.pop();
        // cout<<x<<" "<<check[x]<<"\n";
        for (auto &a: v[x]) {
            if (check[a] != -1) continue;
            check[a] = check[x] + 1;
            q.push(a);
        }
    }

    for (auto &c: check) {
        if (c > 2 || c == -1 || c == 0) continue;
        result++;
    }

    cout << result << "\n";
}