#include<cstring>
#include<iostream>
#include<vector>
#include<queue>
using namespace std;
#define FIO ios_base::sync_with_stdio(0); cin.tie(0);

int n;
vector<vector<int>> map(501, vector<int> (501));
vector<vector<int>> length(501, vector<int> (501, -1));

int dx[4] = {0, 0, 1, -1};
int dy[4] = {1, -1, 0, 0};

int calc(const int x, const int y) {
    int result = 1;
    for (int i=0;i<4;i++) {
        const int nx = x + dx[i];
        const int ny = y + dy[i];
        if (nx < 0 || ny < 0 || n <= nx || n <= ny) continue;
        // н•ҙлӢ№ лҢҖлӮҳл¬ҙлҠ” м§ҖкёҲліҙлӢӨ лҚ” м ҒлӢӨ
        if (map[x][y] <= map[nx][ny]) continue;
        // мқҙлҜё мөңмҶҢ кұ°лҰ¬к°Җ кө¬н•ҙм§„ лҢҖлӮҳл¬ҙ
        if (length[nx][ny] != -1) {
            result = max(result, length[nx][ny] + 1);
        }
        else {
            result = max(result, calc(nx, ny) + 1);
        }
    }

    return length[x][y] = result;
}

int main() {
    FIO;
    cin >> n;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> map[i][j];
        }
    }
    int result = -1;
    for (int i=0;i<n;i++) {
        for (int j=0;j<n;j++) {
            if (length[i][j] != -1) continue;
            result = max(result, calc(i, j));
        }
    }
    cout<<result<<"\n";
}