int dfs(vector<vector<bool>> check, int n, int m, int x, int y) {
    // cout << x << " " << y << "\n";

    if (n <= x || m <= y) return 0;
    if (check[x][y]) {
        int nx = x + (y + 1) / m;
        int ny = (y + 1) % m;

        return dfs(check, n, m, nx, ny);
    }

    int max_sum = 0;

    for (int i = 0; i < 4; i++) {
        bool available = true;
        for (int j = 0; j < 2; j++) {
            int nx = x + v[i][j].x;
            int ny = y + v[i][j].y;
            if (nx < 0 || ny < 0 || n <= nx || m <= ny) {
                available = false;
                break;
            }
            if (check[nx][ny]) {
                available = false;
                break;
            }
        }
        if (!available) continue;
        check[x][y] = true;
        int sum = wood[x][y] * 2;
        for (int j = 0; j < 2; j++) {
            int nx = x + v[i][j].x;
            int ny = y + v[i][j].y;
            check[nx][ny] = true;
            sum += wood[nx][ny];
        }

        int nx = x + (y + 1) / m;
        int ny = (y + 1) % m;

        int result = dfs(check, n, m, nx, ny);
        max_sum = max(result + sum, max_sum);

        check[x][y] = false;
        for (int j = 0; j < 2; j++) {
            int nx = x + v[i][j].x;
            int ny = y + v[i][j].y;
            check[nx][ny] = false;
        }
    }

    int nx = x + (y + 1) / m;
    int ny = (y + 1) % m;

    int result = dfs(check, n, m, nx, ny);

    return max(result, max_sum);
}

