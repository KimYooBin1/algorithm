#include<cstring>
#include<iostream>
#include<queue>
#include<vector>
#include<algorithm>
#include<tuple>
using namespace std;
#define FIO ios_base::sync_with_stdio(false); cin.tie(NULL);
int dx[3] = {1, 0, -1};
int dy[3] = {1, 1, 1};
int main(){
    FIO;
    int n, m;
    cin >> n >> m;
    vector<vector<char>> map(n, vector<char>(m));
    vector<vector<int>> carrot(n, vector<int>(m, -1));
    vector<int> result;
    int x, y;
    for (int i = 0; i < n; i++) {
        string str;
        cin >> str;
        for (int j = 0; j < m; j++) {
            map[i][j] = str[j];
            if(map[i][j] == 'R') x = i, y = j;
        }
    }
    queue<pair<int,int>> q;
    q.push({x, y});
    carrot[x][y] = 0;
    while (!q.empty()) {
        tie(x, y) = q.front(); q.pop();
        for (int i = 0; i < 3; i++) {
            int nx = x + dx[i];
            int ny = y + dy[i];
            if(nx < 0 || ny < 0 || n <= nx || m <= ny) continue;
            if(map[nx][ny] == '#') continue;
            if(map[nx][ny] == 'C'){
                if(carrot[nx][ny] < carrot[x][y] + 1) {
                    q.push({nx, ny});
                    carrot[nx][ny] = carrot[x][y] + 1;
                }
            }else{
                if(carrot[nx][ny] < carrot[x][y]) {
                    q.push({nx, ny});
                    carrot[nx][ny] = carrot[x][y];
                }
            }

            if(map[nx][ny] == 'O') result.push_back(carrot[nx][ny]);
//            q.push({nx, ny});
        }
    }
    if(result.empty()) cout<<-1<<"\n";
    else cout << *max_element(result.begin(), result.end()) << "\n";
}