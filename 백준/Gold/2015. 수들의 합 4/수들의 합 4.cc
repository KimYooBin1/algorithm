#include<cstring>
#include<iostream>
#include<vector>
#include<map>
#include<algorithm>
using namespace std;
#define FIO ios_base::sync_with_stdio(false); cin.tie(NULL);
typedef long long ll;
int main() {
    FIO;
    int n, k;
    cin >> n >> k;
    vector<int> sum(n+1, 0);
    // key : 누적합 값, value : 인덱스
    unordered_map<int,ll> m;
    sum[0] = 0;
    // m[sum[0]].push_back(0);
    for (int i = 0; i < n; i++) {
        int num;
        cin >> num;
        sum[i + 1] = sum[i] + num;
        // m[sum[i + 1]].push_back(i + 1);
    }

    ll result = 0;
    m[0] = 1;
    for (int i = 1; i <= n; i++) {
        int num = sum[i];
        // if (m.find(num) == m.end()) {
        //     m[num]++;
        // }
        int need = num -  k;
        // 해당 누적 합이 없음
        if (m.find(need) != m.end()) {
            result += m[need];
        }
        m[num]++;
    }
    cout << result << "\n";
    return 0;
}